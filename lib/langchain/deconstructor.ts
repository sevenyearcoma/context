export async function deconstructMyth(
  myth: string,
  mythId?: string
): Promise<DeconstructionResult> {
  if (mythId === '6am' || mythId === 'inadequacy') return UNIVERSAL_MOCK

  if (!process.env.ANTHROPIC_API_KEY && !process.env.OPENAI_API_KEY) {
    return UNIVERSAL_MOCK
  }

  try {
    const contextData = await retrieveScientificContext(myth)
    
    let llm: any

    if (process.env.ANTHROPIC_API_KEY) {
      const { ChatAnthropic } = await import('@langchain/anthropic')
      llm = new ChatAnthropic({ model: 'claude-opus-4-6', temperature: 0.7 })
    } else {
      const { ChatOpenAI } = await import('@langchain/openai')
      llm = new ChatOpenAI({ model: 'gpt-4o', temperature: 0.7 })
    }

    const structured = llm.withStructuredOutput(UniversalBoundarySchema)

    const userContent = "Relevant Scientific Context:\n" + contextData + "\n\nThe user's state or problem: \"" + myth + "\""

    const result = await structured.invoke([
      { role: 'system', content: UNIVERSAL_TRUTH_PROMPT },
      { role: 'user', content: userContent },
    ])

    return result as DeconstructionResult
  } catch (err) {
    console.error('[deconstructor] LLM call failed, using mock:', err)
    return UNIVERSAL_MOCK
  }
}
