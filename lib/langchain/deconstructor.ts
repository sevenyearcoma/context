const NodeSchema = z.object({
  id: z.string(),
  type: z.enum(['violation', 'resultBias', 'systemicTruth', 'boundaryDefense']),
  label: z.string().max(80),
  description: z.string().max(250),
  resourceCost: z.number().min(1).max(10),
})

const EdgeSchema = z.object({
  id: z.string(),
  source: z.string(),
  target: z.string(),
  label: z.string().nullable().optional(),
})

const UniversalBoundarySchema = z.object({
  comfortMessage: z.string().max(400).describe(
    'ПЕРВОЕ что видит человек. 1-3 предложения. Мощная валидация без жалости. Утверждение ресурса как конечного ("Ты человек, а не бесконечный источник силы"). Признание дискомфорта поражений или нападок. Тон сильный, защищающий.'
  ),
  safetyAnchor: z.string().max(500).describe(
    'ВТОРОЕ что видит человек. 2-4 предложения. Сдвиг с ожидания 100% безопасности и идеальности на принятие того, что мир несовершенен, данные неполны, а тело право. Фокус на расслаблении и позволении интуиции работать.'
  ),
  nodes: z.array(NodeSchema).min(4).max(8),
  edges: z.array(EdgeSchema).min(3).max(10),
  insight: z.string().max(320).describe('Финальная сборка — переосмысление ситуации через призму универсальных границ и ресурсов.'),
  focusShift: z.string().describe("Сдвиг внимания. Например 'От гиперфокуса на избегании проблем к доверию интуиции', или 'От попытки выжать 150% к защите своей границы ресурсов'"),
})

